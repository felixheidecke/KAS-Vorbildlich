<script>
import { map } from 'lodash'
import { Chart } from 'highcharts-vue'

import imagehosting from '~/plugins/imagehosting.mixin'
import mediaquery from '~/plugins/mediaquery.mixin'

export default {
  name: 'Impfhelferinnen',

  components: {
    Chart
  },

  mixins: [
    imagehosting,
    mediaquery
  ],

  layout: 'article',

  data () {
    return {
      vacData: {}
    }
  },

  computed: {
    chartCredits () {
      return `Quelle: ${this.vacData.meta.source}, Stand: ${this.parseDate(this.vacData.meta.lastUpdate)}`
    },

    chart1 () {
      if (!this.vacData.data) { return {} }

      return {
        chart: {
          type: 'bar'
        },
        title: {
          text: 'Impfquote in der Bevölkerung'
        },
        xAxis: {
          categories: ['Deutschland', 'Berlin']
        },
        yAxis: {
          title: {
            enabled: false
          },
          max: 100,
          labels: {
            formatter () {
              return this.value + '%'
            }
          }
        },
        credits: {
          text: this.chartCredits
        },
        series: [{
          name: 'Erstimpfung',
          data: [
            this.germany.firstDosePercentage,
            this.berlin.firstDosePercentage
          ]
        }, {
          name: 'Zweitimpfung',
          data: [
            this.germany.secondDosePercentage,
            this.berlin.secondDosePercentage
          ]
        }]
      }
    },

    chart2 () {
      return {
        chart: {
          type: 'pie'
        },
        title: {
          text: 'Erstimpfung in Berlin nach Impfstoff'
        },
        credits: {
          text: this.chartCredits
        },
        series: [{
          name: 'Impfstoff',
          className: 'uk-text-capitalize',
          data: map(this.berlin.firstDoseByType, (value, key) => {
            return {
              name: key,
              y: value
            }
          })
        }]
      }
    },

    chart3 () {
      return {
        chart: {
          type: 'pie'
        },
        title: {
          text: 'Zweitimpfung in Berlin nach Impfstoff'
        },
        credits: {
          text: this.chartCredits
        },
        series: [{
          name: 'Impfstoff',
          className: 'uk-text-capitalize',
          data: map(this.berlin.secondDoseByType, (value, key) => {
            return {
              name: key,
              y: value
            }
          })
        }]
      }
    },

    hasVacData () {
      return 'data' in this.vacData
    },

    germany () {
      if (!this.vacData.data) { return {} }
      const data = this.vacData.data

      return {
        firstDosePercentage: Math.round((100 / 83020000) * data.vaccinated),
        secondDosePercentage: Math.round((100 / 83020000) * data.secondVaccination.vaccinated)
      }
    },

    berlin () {
      if (!this.vacData.data) { return {} }
      const data = this.vacData.data.states.BE

      return {
        firstDosePercentage: Math.round((100 / 3645000) * data.vaccinated),
        secondDosePercentage: Math.round((100 / 3645000) * data.secondVaccination.vaccinated),
        firstDoseByType: data.secondVaccination.vaccination,
        secondDoseByType: data.vaccination
      }
    }
  },

  async mounted () {
    const req = await fetch('https://api.corona-zahlen.org/vaccinations')
    this.vacData = await req.json()
  },

  methods: {
    formatDecimal (number) {
      return new Intl.NumberFormat('de-DE', { style: 'decimal' }).format(number)
    },

    formatPercent (number) {
      return Math.Round(number) + '%'
    },

    parseDate (date) {
      const dateObject = Date.parse(date)
      const newDate = new Date(dateObject)

      return newDate.toLocaleDateString('de-DE')
    }
  }
}
</script>

