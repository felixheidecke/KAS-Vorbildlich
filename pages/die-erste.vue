      p Kurz vor ihrer Kandidatur trennt sie sich von ihrem Mann. Auch das wird im Wahlkampf zum Thema gemacht. „Wie soll sie denn dann so ein Amt innehaben, wenn sie schon in ihrer privaten Beziehung scheitert? Und was ist, wenn ihr Mann ihr dann Probleme bereitet? Wie soll sie das dann emotional überhaupt leisten, diese Aufgabe durchzuführen? Das sind Punkte, die ich immer wieder gehört habe.“ Bei männlichen Kandidaten, glaubt sie, wäre das nie von so großem Interesse. Trotzdem entschließt sie sich irgendwann dazu, offen darüber zu sprechen, dass ihre Beziehung gescheitert ist, wie sie Berufliches und Privates vereinbart, wie sie ihr Leben jetzt gestaltet. „Anscheinend ist das ein gesellschaftliches Thema, was ich jetzt auch nicht totschweigen möchte. Muss sich heutzutage Familie und Karriere ausschließen? Wollen wir das für unsere Stadt hier vor Ort? Und sind Menschen, weil sie jung sind, weniger fähig, Ämter zu bekleiden?“ Diekmann stößt damit eine Diskussion an und trägt sie aus. Sie glaubt, damit noch etwas anderes Wichtiges wieder in den Fokus der Politik vor Ort gerückt zu haben: Menschlichkeit, Fehlbarkeit, Scheitern. Dass Lebenswege unterschiedlich sind und nicht für jeden das gleiche Modell funktioniert.

