      p Eliza Diekmann, heute 34, lebt zu dieser Zeit noch nicht lange in Coesfeld. Erst vor fünf Jahren, als sie zum ersten Mal schwanger ist, zog sie hierher zu ihrem Mann. Vorher arbeitet sie als Wirtschaftsredakteurin bei einem Verlag in Frankfurt, bis zum Mutterschutz führt sie eine Fernbeziehung. „Als ich hier nach Coesfeld gekommen bin, habe ich schon gesagt, ich muss hier richtig kreativ werden, um langfristig glücklich zu sein. Ich muss etwas finden, wo ich meine Leidenschaften zusammenbringe und auch beruflich gefordert werde.“ Bei ihr zu Hause gab es seitdem den Running Gag, eigentlich müsste sie ja Bürgermeisterin werden, damit sie einen Job hat, der sie voll ausfüllt. Und das, obwohl sie zuvor noch nie politisch aktiv war. Zwar engagiert sie sich in Coesfeld als Mitbegründerin der Initiative „Parents for future“ und setzt sich dadurch in der Gemeinschaft ein, aber eine Karriere als Politikerin - dieser Gedanke war neu. Damals war das alles noch ein Scherz, doch, wie das oft so ist, steckt in jedem Spaß eben auch ein bisschen Ernst.

      p Coesfeld, im westlichen Münsterland, ist mit 36.000 Einwohnern eine sogenannte Mittelstadt. Nicht groß, nicht klein, so mittel eben. Bis zur letzten Wahl regierte hier Heinz Öhmann von der CDU. Insgesamt 17 Jahre lang. Öhmann will bei der Kommunalwahl 2020 nicht erneut antreten. Das schafft Raum für Veränderung, für neue Kandidaten, für neue Kandidatinnen. Während die CDU erneut einen Mann ins Rennen schickt, tun sich andere Fraktionen schwer mit der Nominierung. Aus dem Gedankenspiel von Eliza Diekmann wird zu dieser Zeit zum ersten Mal so etwas wie eine Chance, eine Lücke, die sich auftut.

